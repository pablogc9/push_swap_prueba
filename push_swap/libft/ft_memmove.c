#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s;
	char	*d;
	size_t	i;
	int		*ptr;

	s = (char *)src;
	d = (char *)dst;
	i = 0;
	ptr = NULL;
	if (!dst && !(void *)src)
		return (NULL);
	if (!dst || !(void *)src)
		*ptr = 1;
	if (d > s)
		while (len-- > 0)
			d[len] = s[len];
	else
	{
		while (i < len)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (dst);
}
